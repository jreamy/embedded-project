int main(void)
{
    uint16_t data[3] = {10,10,10};
    float volts[3];
    /* Initialize the SAM system */
    SystemInit();
    adc_init();
    emg_init();


    /* Replace with your application code */
    while (1) {
        emg_read(&data);
        while(!emg_read_complete());
        for (uint8_t idx = 0; idx < 3; idx++) {
            volts[idx] = 3.3 * data[idx] / 4095.0;
        }
        data[0] = data[0];
    }
}

